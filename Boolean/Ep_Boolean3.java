public class Ep_Boolean3 
{
    public static void main(String[] args) 
    {
        boolean result = 10>5;
        if(result)
        {
            System.out.println("10 > 5");
            System.out.println("True");
        }else
        {
            System.out.println("10 > 5");
            System.out.println("False");
        }   
    }
}
