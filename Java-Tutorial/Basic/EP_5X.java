
public class EP_5X 
{
    public static void main(String args [])
    {
        String str = "Welcome to Rattana Bundit University";
        char ch1 = str.charAt(0);
        char ch2 = str.charAt(6);
        char ch3 = str.charAt(11);
        System.out.println("Character at 0 index is: "+ch1); //! แสดงตัวที่ 0 ของ บรรทัด ที่ 6 ที่เราทำการ charAt 0
        System.out.println("Character at 6 index is:"+ch2); //! แสดงตัวที่ 6 ของ บรรทัด ที่ 6 ที่เราทำการ charAt 6
        System.out.println("Character at 11 index is:"+ch3);//! แสดงตัวที่ 11 ของ บรรทัด ที่ 6 ที่เราทำการ charAt 11
    }
}
